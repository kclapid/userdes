		<p>

When miss Jen told us that we will be having a quiz, I really had no idea what to study. I didn’t expect that our quiz is 
a bit annoying. Our quiz is to figure out how Userinyerface works, and yes, as for the title, it is indeed the worst user 
interface ever. Before miss Jen left us, she said good luck to find the pattern, and at that point I knew that the quiz is 
tricky. At the sign up page, I thought it’s just a typical sign up page where I should enter my name, email address and 
password. There were instructions below the box and I was figuring out what was a Cyrillic character as one of the 
requirements for password. The class was hype and loud voices were all over the lab, struggling to get through the sign up 
page. We opened the terms and condition, scrolled all the way down just to find out that the statement was ‘I do not 
accept the Terms and Condition’. It’s very annoying to scroll down with a very slow movement, but it was useless. The 
arrangement of the text box is also confusing. The password is the first one to fill out which is very unusual, since it 
always requires email address first. When I clicked on the text box, I didn’t realize that it was unmasked while I was 
typing my real password, and I didn’t know that I should delete the word password first. The main button is ‘cancel’ which 
is also very unusual, and the ‘next’ button is just on the left side. As the process goes on, it’s getting easier to 
figure out the pattern since we know that the UI is tricky.</p>
<p>
I was pressured since the goal is to fill in the form as fast as we can. It didn’t just test our logical thinking skill 
and eye movement speed, but it also tested our patience. After accomplishing the form, I realized how significant design 
is for a website. The arrangement of the buttons, textbox, hyperlinks, color used, and instructions really matter. It is 
an achievement if the user utilized the website smoothly and without struggle. </p>

	
